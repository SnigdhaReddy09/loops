// for loop
// increment
for(var a=1;a<3;a++){
    console.log(a)
}
// 1,2

for(var a=1;a<=7;a++){
    console.log(a)
}
// 1-7

for(var a=1;a<=10;a++){
    console.log(a)
}
// 1-10

for(var a=7;a<12;a++){
    console.log(a)
}
// 7-11

for(var a=17;a<3;a++){
    console.log(a)
}
// no output

// decrement
for(var a=11;a>6;a--){
    console.log(a)
}
// 11-7

for(var a=24;a>=15;a--){
    console.log(a)
}
// 24-15

