// while loop
// increment
var a=1;
while(a<=7){
    console.log(a)
    a++
}
// 1-7

// decrement
var a=10;
while(a>=6){
    console.log(a)
    a--
}
// 10-6

// ***
var i=1;
while(i<4){
    i++
}
console.log(i)
// 4

// do-while
// increment
var i=1;
do{
i++
console.log(i)
}while(i<=5);
// 2-6

// decrement
var a=10;
do{
a--
console.log(a)
}while(a>=7);
// 9-6

